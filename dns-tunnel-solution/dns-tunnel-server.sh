            echo -e "    Status: ${RED}● STOPPED${NC}"
        fi
    fi

    # Port 53
    echo ""
    echo -e "  ${BOLD}Port 53:${NC}"
    local port53
    port53=$(ss -ulnp 2>/dev/null | grep ':53 ' | head -3)
    if [[ -n "$port53" ]]; then
        echo -e "    ${GREEN}● Listening${NC}"
        echo "$port53" | while read -r line; do
            echo -e "    ${DIM}$line${NC}"
        done
    else
        echo -e "    ${YELLOW}● Not listening${NC} (iptables redirect may be active)"
    fi

    # Port 5300
    echo ""
    echo -e "  ${BOLD}Port $DNSTT_PORT (dnstt):${NC}"
