            echo "  2) SSH tunnel"
            read -rp "  New mode: " new_mode
            if [[ "$new_mode" == "1" ]]; then
                CONF_MODE="socks"
                install_dante_socks
            elif [[ "$new_mode" == "2" ]]; then
                CONF_MODE="ssh"
            fi
            save_config
            create_systemd_service
            systemctl restart "$SYSTEMD_SERVICE"
            print_ok "Mode changed to $CONF_MODE"
            ;;
        3)
            echo ""
            read -rp "  New domain [${CONF_DOMAIN}]: " new_domain
            if [[ -n "$new_domain" ]]; then
                CONF_DOMAIN="$new_domain"
                generate_keys
                save_config
                create_systemd_service
                systemctl restart "$SYSTEMD_SERVICE"
                print_ok "Domain changed. New keys generated."
            fi
            ;;
        4)
            create_socks_user
            save_config
            print_ok "User credentials updated"
            ;;
        5)
            generate_keys
            save_config
            create_systemd_service
            systemctl restart "$SYSTEMD_SERVICE"
            print_ok "Keys regenerated and service restarted"
            print_warn "Clients need the new public key!"
            ;;
        6)
            run_setup
            ;;
        0) return ;;
    esac

    press_enter
}

uninstall() {
    print_header "Uninstall" "Remove all DNS tunnel components"

    print_warn "This will remove:"
    echo "    - dnstt-server binary and service"
    echo "    - Configuration and keys"
    echo "    - iptables rules"
    echo "    - Dante SOCKS proxy (if installed)"
    echo ""

    if ! confirm "Are you sure?"; then
        return
    fi

    # Stop services
    systemctl stop "$SYSTEMD_SERVICE" 2>/dev/null
    systemctl disable "$SYSTEMD_SERVICE" 2>/dev/null
    systemctl stop "$DANTE_SERVICE" 2>/dev/null

    # Remove files
    rm -f "$SERVICE_FILE"
    rm -f "$DNSTT_BINARY"
    rm -rf "$CONFIG_DIR"
    systemctl daemon-reload

    # Remove iptables rules
    iptables -t nat -D PREROUTING -p udp --dport 53 -j REDIRECT --to-port "$DNSTT_PORT" 2>/dev/null
    iptables -t nat -D PREROUTING -p tcp --dport 53 -j REDIRECT --to-port "$DNSTT_PORT" 2>/dev/null

    # Remove user
    userdel "$DNSTT_USER" 2>/dev/null

    print_ok "Uninstall complete"
    print_warn "DNS records in your registrar must be removed manually"
    print_warn "To re-enable systemd-resolved:"
    print_dim "  chattr -i /etc/resolv.conf"
    print_dim "  rm /etc/resolv.conf && ln -s ../run/systemd/resolve/stub-resolv.conf /etc/resolv.conf"
    print_dim "  systemctl unmask systemd-resolved && systemctl enable --now systemd-resolved"

    press_enter
}

