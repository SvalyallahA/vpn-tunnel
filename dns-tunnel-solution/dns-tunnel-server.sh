# Root check (except for --help)
if [[ "${1:-}" != "--help" && "${1:-}" != "-h" && $EUID -ne 0 ]]; then
    echo -e "${RED}Error:${NC} This script must be run as root"
    echo "  sudo bash $0"
    exit 1
fi

case "${1:-}" in
    setup|install)
        run_setup
        ;;
    status)
        if is_configured; then load_config; fi
        show_status
        ;;
    config|show)
        show_config
        ;;
    logs|log)
        show_logs
        ;;
    start)
        start_services
        ;;
    stop)
        stop_services
        ;;
    restart)
        restart_services
        ;;
    serve|files)
        setup_file_server
        ;;
    -h|--help)
        echo ""
        echo "  DNS Tunnel Server Manager v$SCRIPT_VERSION"
        echo ""
        echo "  Usage: sudo bash $0 [command]"
        echo ""
        echo "  Commands:"
        echo "    (none)     Interactive menu"
        echo "    setup      Run the setup wizard"
        echo "    status     Show service status"
        echo "    config     Show configuration and connection info"
        echo "    logs       View service logs"
        echo "    start      Start services"
        echo "    stop       Stop services"
        echo "    restart    Restart services"
        echo "    serve      Start client file server"
        echo ""
        ;;
    *)
        main_menu
        ;;
esac
