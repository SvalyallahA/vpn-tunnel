show_status() {
    print_header "Service Status" ""

    # dnstt-server
    echo -e "  ${BOLD}dnstt-server:${NC}"
    if is_running; then
        echo -e "    Status: ${GREEN}● RUNNING${NC}"
    else
        echo -e "    Status: ${RED}● STOPPED${NC}"
    fi

    local pid
    pid=$(pgrep -f "dnstt-server" 2>/dev/null | head -1)
    if [[ -n "$pid" ]]; then
        echo -e "    PID:    $pid"
        local mem
        mem=$(ps -p "$pid" -o rss= 2>/dev/null | awk '{printf "%.1f MB", $1/1024}')
        echo -e "    Memory: $mem"
        local uptime_info
        uptime_info=$(ps -p "$pid" -o etime= 2>/dev/null | xargs)
        echo -e "    Uptime: $uptime_info"
    fi

    # Dante (if SOCKS mode)
    if [[ -f "$DANTE_CONF" ]]; then
        echo ""
        echo -e "  ${BOLD}Dante SOCKS proxy:${NC}"
        if systemctl is-active --quiet "$DANTE_SERVICE" 2>/dev/null; then
            echo -e "    Status: ${GREEN}● RUNNING${NC}"
        else
            echo -e "    Status: ${RED}● STOPPED${NC}"
        fi
    fi

    # Port 53
    echo ""
    echo -e "  ${BOLD}Port 53:${NC}"
    local port53
    port53=$(ss -ulnp 2>/dev/null | grep ':53 ' | head -3)
    if [[ -n "$port53" ]]; then
        echo -e "    ${GREEN}● Listening${NC}"
        echo "$port53" | while read -r line; do
            echo -e "    ${DIM}$line${NC}"
        done
    else
        echo -e "    ${YELLOW}● Not listening${NC} (iptables redirect may be active)"
    fi

    # Port 5300
    echo ""
    echo -e "  ${BOLD}Port $DNSTT_PORT (dnstt):${NC}"
    local port5300
    port5300=$(ss -ulnp 2>/dev/null | grep ":${DNSTT_PORT} " | head -3)
    if [[ -n "$port5300" ]]; then
        echo -e "    ${GREEN}● Listening${NC}"
    else
        echo -e "    ${RED}● Not listening${NC}"
    fi

    # iptables
    echo ""
    echo -e "  ${BOLD}iptables NAT rules:${NC}"
    local nat_rules
    nat_rules=$(iptables -t nat -L PREROUTING -n 2>/dev/null | grep -c "dpt:53")
    echo -e "    Port 53 redirects: $nat_rules"

    # Config
    if is_configured; then
        load_config
        echo ""
        echo -e "  ${BOLD}Configuration:${NC}"
        echo -e "    Domain:  ${CONF_DOMAIN}"
        echo -e "    Mode:    ${CONF_MODE}"
        echo -e "    MTU:     ${CONF_MTU}"
        echo -e "    IP:      ${CONF_SERVER_IP}"
    fi

    echo ""
}

show_config() {
    if ! is_configured; then
        print_err "No configuration found. Run setup first."
        return 1
    fi

    load_config
    show_summary
}

show_logs() {
    print_header "Service Logs" "Press Ctrl+C to exit"

    echo ""
    echo "  1) dnstt-server logs (live)"
    echo "  2) dnstt-server logs (last 50 lines)"
    echo "  3) Dante SOCKS proxy logs"
    echo "  4) All logs combined"
    echo "  0) Back"
    echo ""

    read -rp "  Choose [1]: " log_choice
    log_choice="${log_choice:-1}"

    case "$log_choice" in
        1)
            echo ""
            print_info "Showing live logs (Ctrl+C to stop)..."
            echo ""
            journalctl -u "$SYSTEMD_SERVICE" -f --no-pager
            ;;
        2)
            echo ""
            journalctl -u "$SYSTEMD_SERVICE" -n 50 --no-pager
            press_enter
            ;;
        3)
            echo ""
            if systemctl list-units --type=service 2>/dev/null | grep -q "$DANTE_SERVICE"; then
                journalctl -u "$DANTE_SERVICE" -n 50 --no-pager
            else
                print_warn "Dante service not found"
            fi
            press_enter
            ;;
        4)
            echo ""
            print_info "Showing live combined logs (Ctrl+C to stop)..."
            echo ""
            journalctl -u "$SYSTEMD_SERVICE" -u "$DANTE_SERVICE" -f --no-pager
            ;;
        0) return ;;
    esac
}

start_services() {
    print_section "Starting Services"

    if ! is_configured; then
        print_err "No configuration found. Run setup first."
        return 1
    fi

    systemctl start "$SYSTEMD_SERVICE"
    sleep 1

    if is_running; then
        print_ok "dnstt-server started"
    else
        print_err "dnstt-server failed to start"
        print_info "Check: journalctl -u $SYSTEMD_SERVICE -n 20"
    fi

    # Start Dante if SOCKS mode
    load_config
    if [[ "${CONF_MODE:-}" == "socks" ]]; then
        systemctl start "$DANTE_SERVICE" 2>/dev/null
        if systemctl is-active --quiet "$DANTE_SERVICE" 2>/dev/null; then
            print_ok "Dante SOCKS proxy started"
        fi
    fi

    press_enter
}

stop_services() {
    print_section "Stopping Services"

    systemctl stop "$SYSTEMD_SERVICE" 2>/dev/null
    if ! is_running; then
        print_ok "dnstt-server stopped"
    else
        print_err "dnstt-server may still be running"
    fi

    systemctl stop "$DANTE_SERVICE" 2>/dev/null
    print_ok "Dante SOCKS proxy stopped"

    press_enter
}

restart_services() {
    print_section "Restarting Services"

    systemctl restart "$SYSTEMD_SERVICE"
    sleep 2

    if is_running; then
        print_ok "dnstt-server restarted"
    else
        print_err "dnstt-server failed to start"
        journalctl -u "$SYSTEMD_SERVICE" -n 5 --no-pager 2>/dev/null
    fi

    load_config 2>/dev/null
    if [[ "${CONF_MODE:-}" == "socks" ]]; then
        systemctl restart "$DANTE_SERVICE" 2>/dev/null
        if systemctl is-active --quiet "$DANTE_SERVICE"; then
            print_ok "Dante restarted"
        fi
    fi

    press_enter
}

reconfigure() {
    print_header "Reconfigure" "Change settings without full reinstall"

    if ! is_configured; then
        print_warn "No existing config. Running full setup instead."
        run_setup
        return
    fi

    load_config

    echo ""
    echo "  What would you like to change?"
    echo ""
    echo "  1) MTU value          (current: ${CONF_MTU})"
    echo "  2) Tunnel mode        (current: ${CONF_MODE})"
    echo "  3) Domain             (current: ${CONF_DOMAIN})"
    echo "  4) Tunnel user/pass   (current: ${CONF_SOCKS_USER:-none})"
    echo "  5) Regenerate keys"
    echo "  6) Full re-setup"
    echo "  0) Back"
    echo ""

    read -rp "  Choose [0]: " reconf_choice

    case "$reconf_choice" in
        1)
            echo ""
            echo -e "  Current MTU: ${CYAN}${CONF_MTU}${NC}"
            echo -e "  ${GREEN}512${NC}=max compat | ${YELLOW}800${NC}=balanced | ${CYAN}1024${NC}=faster"
            read -rp "  New MTU [$CONF_MTU]: " new_mtu
            if [[ -n "$new_mtu" ]]; then
                CONF_MTU="$new_mtu"
                save_config
                create_systemd_service
                systemctl restart "$SYSTEMD_SERVICE"
                sleep 1
                if is_running; then
                    print_ok "MTU changed to $CONF_MTU and service restarted"
                else
                    print_err "Service failed to restart"
                fi
            fi
            ;;
        2)
            echo ""
            echo "  1) SOCKS proxy"
            echo "  2) SSH tunnel"
            read -rp "  New mode: " new_mode
            if [[ "$new_mode" == "1" ]]; then
                CONF_MODE="socks"
                install_dante_socks
            elif [[ "$new_mode" == "2" ]]; then
                CONF_MODE="ssh"
            fi
            save_config
            create_systemd_service
            systemctl restart "$SYSTEMD_SERVICE"
            print_ok "Mode changed to $CONF_MODE"
            ;;
        3)
            echo ""
            read -rp "  New domain [${CONF_DOMAIN}]: " new_domain
            if [[ -n "$new_domain" ]]; then
                CONF_DOMAIN="$new_domain"
                generate_keys
                save_config
                create_systemd_service
                systemctl restart "$SYSTEMD_SERVICE"
                print_ok "Domain changed. New keys generated."
            fi
            ;;
        4)
            create_socks_user
            save_config
            print_ok "User credentials updated"
            ;;
        5)
            generate_keys
            save_config
            create_systemd_service
            systemctl restart "$SYSTEMD_SERVICE"
            print_ok "Keys regenerated and service restarted"
            print_warn "Clients need the new public key!"
            ;;
        6)
            run_setup
            ;;
        0) return ;;
    esac

    press_enter
}

uninstall() {
    print_header "Uninstall" "Remove all DNS tunnel components"

    print_warn "This will remove:"
    echo "    - dnstt-server binary and service"
    echo "    - Configuration and keys"
    echo "    - iptables rules"
    echo "    - Dante SOCKS proxy (if installed)"
    echo ""

    if ! confirm "Are you sure?"; then
        return
    fi

    # Stop services
    systemctl stop "$SYSTEMD_SERVICE" 2>/dev/null
    systemctl disable "$SYSTEMD_SERVICE" 2>/dev/null
    systemctl stop "$DANTE_SERVICE" 2>/dev/null

    # Remove files
    rm -f "$SERVICE_FILE"
    rm -f "$DNSTT_BINARY"
    rm -rf "$CONFIG_DIR"
    systemctl daemon-reload

    # Remove iptables rules
    iptables -t nat -D PREROUTING -p udp --dport 53 -j REDIRECT --to-port "$DNSTT_PORT" 2>/dev/null
    iptables -t nat -D PREROUTING -p tcp --dport 53 -j REDIRECT --to-port "$DNSTT_PORT" 2>/dev/null

    # Remove user
    userdel "$DNSTT_USER" 2>/dev/null

    print_ok "Uninstall complete"
    print_warn "DNS records in your registrar must be removed manually"
    print_warn "To re-enable systemd-resolved:"
    print_dim "  chattr -i /etc/resolv.conf"
    print_dim "  rm /etc/resolv.conf && ln -s ../run/systemd/resolve/stub-resolv.conf /etc/resolv.conf"
    print_dim "  systemctl unmask systemd-resolved && systemctl enable --now systemd-resolved"

    press_enter
}

