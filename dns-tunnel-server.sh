show_menu() {
    clear 2>/dev/null || true

    # Status indicator
    local status_icon status_text
    if is_running; then
        status_icon="${GREEN}●${NC}"
        status_text="${GREEN}RUNNING${NC}"
    elif is_configured; then
        status_icon="${RED}●${NC}"
        status_text="${RED}STOPPED${NC}"
    else
        status_icon="${YELLOW}●${NC}"
        status_text="${YELLOW}NOT INSTALLED${NC}"
    fi

    echo ""
    echo -e "${CYAN}  ╔══════════════════════════════════════════════╗${NC}"
    echo -e "${CYAN}  ║     DNS Tunnel Server Manager  v${SCRIPT_VERSION}      ║${NC}"
    echo -e "${CYAN}  ║     Optimized for censored networks         ║${NC}"
    echo -e "${CYAN}  ╚══════════════════════════════════════════════╝${NC}"
    echo ""
    echo -e "  Status: $status_icon $status_text"

    if is_configured; then
        load_config 2>/dev/null
        echo -e "  Domain: ${CYAN}${CONF_DOMAIN:-?}${NC}  MTU: ${CYAN}${CONF_MTU:-?}${NC}  Mode: ${CYAN}${CONF_MODE:-?}${NC}"
    fi

    echo ""
    print_line 50 "─"
    echo ""

    if ! is_configured; then
        echo -e "  ${BOLD}1)${NC}  Install & Setup          ${GREEN}← start here${NC}"
    else
        echo -e "  ${BOLD}1)${NC}  Install & Setup          (re-run wizard)"
    fi

    echo -e "  ${BOLD}2)${NC}  Show Status"
    echo -e "  ${BOLD}3)${NC}  Show Configuration       (connection info + share)"
    echo -e "  ${BOLD}4)${NC}  View Logs"
    echo ""
    echo -e "  ${BOLD}5)${NC}  Start Services"
    echo -e "  ${BOLD}6)${NC}  Stop Services"
    echo -e "  ${BOLD}7)${NC}  Restart Services"
    echo ""
    echo -e "  ${BOLD}8)${NC}  Reconfigure              (change MTU, mode, domain...)"
    echo -e "  ${BOLD}9)${NC}  Uninstall"
    echo ""
    echo -e "  ${BOLD}0)${NC}  Exit"
    echo ""
    print_line 50 "─"
}

main_menu() {
    while true; do
        show_menu
        read -rp "  Choose [0-9]: " choice
        echo ""

        case "$choice" in
            1) run_setup ;;
            2) show_status; press_enter ;;
            3) show_config; press_enter ;;
            4) show_logs ;;
            5) start_services ;;
            6) stop_services ;;
            7) restart_services ;;
            8) reconfigure ;;
            9) uninstall ;;
            0)
                echo -e "  ${DIM}Goodbye${NC}"
                echo ""
                exit 0
                ;;
            *)
                print_warn "Invalid option"
                sleep 1
                ;;
        esac
    done
}

